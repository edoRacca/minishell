#include "libft.h"

char	*ft_strcpy(char *d, const char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
		return (d);
	while (s[i] != '\0')
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
