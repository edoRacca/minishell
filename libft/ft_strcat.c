#include "libft.h"

char	*ft_strcat(char *d, const char *s)
{
	int	i;
	int	k;

	k = 0;
	i = ft_strrlen(d);
	while (s[k] != '\0')
	{
		d[i++] = s[k++];
	}
	d[i] = '\0';
	return (d);
}
